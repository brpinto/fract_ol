#include <libft.h>

int	ft_word_count(char *s, char c)
{
	int	i;
	int	wordc;

	i = 0;
	wordc = 0;
	while (s[i] == c)
		i++;
	while (s[i] != '\0')
	{
		if ((s[i] != c) && (s[i + 1] == c || s[i + 1] == '\0'))
			wordc++;
		i++;
	}
	return (wordc);
}
